const numeros = [100, 200, 300, 400, 500, 600]


// sintaxe do For consiste em três expressões dentro do parenteses!

// 1° expressão é executada uma única vez;
// 2° expressão é a condição de execução;
// 3° expressão é executada sempre ao final do bloco;
for (let indice = 0; indice < numeros.length; indice++) {
    console.log(numeros[indice]);
}